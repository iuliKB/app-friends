-- =============================================================
-- VERIFICATION QUERIES
-- Run these after seeding to confirm everything looks right.
-- =============================================================

-- Should return 6 rows
SELECT username, display_name FROM profiles ORDER BY username;

-- Should show free x3, busy x2, maybe x1
SELECT p.username, s.status, s.context_tag
FROM statuses s JOIN profiles p ON p.id = s.user_id
ORDER BY s.status, p.username;

-- Should show 6 friendships (5 accepted, 1 pending)
SELECT
  p1.username AS requester,
  p2.username AS addressee,
  f.status
FROM friendships f
JOIN profiles p1 ON p1.id = f.requester_id
JOIN profiles p2 ON p2.id = f.addressee_id
ORDER BY f.status, p1.username;

-- Should show 2 plans with member counts
SELECT pl.title, pl.scheduled_for, COUNT(pm.user_id) AS members
FROM plans pl
JOIN plan_members pm ON pm.plan_id = pl.id
GROUP BY pl.id, pl.title, pl.scheduled_for
ORDER BY pl.scheduled_for;

-- Should show 15 messages across 3 channels (6 plan_a + 5 plan_b + 4 dm)
SELECT
  COALESCE(pl.title, 'DM: ' || p1.username || ' ↔ ' || p2.username) AS channel,
  COUNT(m.id) AS message_count
FROM messages m
LEFT JOIN plans pl ON pl.id = m.plan_id
LEFT JOIN dm_channels dc ON dc.id = m.dm_channel_id
LEFT JOIN profiles p1 ON p1.id = dc.user_a
LEFT JOIN profiles p2 ON p2.id = dc.user_b
GROUP BY channel
ORDER BY channel;
